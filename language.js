// Language management class
class LanguageManager {
  constructor() {
    this.currentLanguage = this.getStoredLanguage() || 'en';
    this.init();
  }

  init() {
    // Apply translations immediately
    this.applyTranslations();

    // Set up language toggle button if it exists
    this.setupLanguageToggle();

    // Listen for language change events
    document.addEventListener('languageChange', (e) => {
      this.setLanguage(e.detail.language);
    });
  }

  getStoredLanguage() {
    return localStorage.getItem('selectedLanguage');
  }

  setStoredLanguage(language) {
    localStorage.setItem('selectedLanguage', language);
  }

  setLanguage(language) {
    if (translations[language]) {
      this.currentLanguage = language;
      this.setStoredLanguage(language);
      this.applyTranslations();

      // Dispatch custom event for other components
      document.dispatchEvent(new CustomEvent('languageChanged', {
        detail: { language }
      }));
    }
  }

  getCurrentLanguage() {
    return this.currentLanguage;
  }

  translate(key, fallback = '') {
    const keys = translations[this.currentLanguage];
    return keys && keys[key] ? keys[key] : (fallback || key);
  }

  applyTranslations() {
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
      const key = element.getAttribute('data-translate');
      const translation = this.translate(key);

      // Skip elements that are dynamically updated by JavaScript
      if (element.id === 'rotating-subtitle') {
        // For rotating subtitle, only set initial text if not already set by JavaScript
        if (!element.hasAttribute('data-dynamic-content')) {
          element.textContent = translation;
        }
        return;
      }

      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        if (element.type === 'submit' || element.type === 'button') {
          element.value = translation;
        } else {
          element.placeholder = translation;
        }
      } else {
        element.textContent = translation;
      }
    });

    // Update all elements with data-translate-html attribute
    document.querySelectorAll('[data-translate-html]').forEach(element => {
      const key = element.getAttribute('data-translate-html');
      element.innerHTML = this.translate(key);
    });

    // Update document language attribute
    document.documentElement.lang = this.currentLanguage === 'hi' ? 'hi' : 'en';
  }

  setupLanguageToggle() {
    const toggleButton = document.getElementById('language-toggle');
    if (toggleButton) {
      toggleButton.addEventListener('click', () => {
        const newLanguage = this.currentLanguage === 'en' ? 'hi' : 'en';
        this.setLanguage(newLanguage);
      });

      // Update button text
      this.updateToggleButton(toggleButton);
    }
  }

  updateToggleButton(button) {
    // Show current language on button, indicate what clicking will do
    button.textContent = this.currentLanguage === 'en' ? 'हिन्दी' : 'English';
    button.title = this.currentLanguage === 'en' ? 'हिन्दी में बदलें' : 'Switch to English';
  }

  // Method to get all translation keys for a language
  getAllTranslations(language = null) {
    const lang = language || this.currentLanguage;
    return translations[lang] || {};
  }

  // Method to add new translations
  addTranslation(language, key, value) {
    if (!translations[language]) {
      translations[language] = {};
    }
    translations[language][key] = value;
  }

  // Method to update existing translations
  updateTranslation(language, key, value) {
    if (translations[language] && translations[language][key]) {
      translations[language][key] = value;
      if (language === this.currentLanguage) {
        this.applyTranslations();
      }
    }
  }
}

// Create global instance
const languageManager = new LanguageManager();

// Export for use in other scripts
window.LanguageManager = LanguageManager;
window.languageManager = languageManager;
window.translate = (key, fallback) => languageManager.translate(key, fallback);